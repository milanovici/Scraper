package main;

/**
 *
 * @author Igor Milanovic <igor.milanovic204@gmail.com>
 */
public class YelpStudio {

    private String name;
    private String price;
    private String location;
    private String number;
    private String neighboorhoodList;
    private String category;
    private String link;
    private String reviews;
    private String stars;

    public YelpStudio() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getReviews() {
        return reviews;
    }

    public void setReviews(String reviews) {
        this.reviews = reviews;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getNeighboorhoodList() {
        return neighboorhoodList;
    }

    public void setNeighboorhoodList(String neighboorhoodList) {
        this.neighboorhoodList = neighboorhoodList;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getStars() {
        return stars;
    }

    public void setStars(String stars) {
        this.stars = stars;
    }

}
